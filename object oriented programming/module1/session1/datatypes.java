public class datatypes {
    public static void main(String[] args)
    {
        int y = 999999;

        long l = 1000000000;

        short x = 1000;

        float z = 0.9f;     // explicitly define float value by appending 'f' after the value

        double d = 4.3456;  // default decimal data type in java, that's why 'f' after float values

        char a = 't';
        char b = '7';

        boolean b1 = true;
        boolean b2 = 3<2;
        System.out.println(b2);


    }
}
